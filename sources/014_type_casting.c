#include <stdio.h>

int main() {
    int a = 5;
    int b = 2;

    /*
        จาก code ดังกล่าว 
        compiler จะมองว่าการแปรงชนิดข้อมูลมีลำดับความสำคัญมากที่สุด
        มันจึงเปลี่ยนแค่ a จาก 5 เป็น 5.0 ก่อน แล้วค่อยนำไปหาร 2 และส่งคืน 2.5
        
        โดย compiler จะดูจากชนิดข้อมูลของทั้ง 2 ว่าอันไหนมีขนาดข้อมูลที่ใหญ่กว่า จึงจะ return ชนิดข้อมูลนั้น
     */
    double c = (double)a / b;
    printf("%d / %d = %f\n", a, b, c);

    return 0;
}
